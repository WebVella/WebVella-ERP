function seq(arr, callback, index) {
    if (typeof index === 'undefined') {
        index = 0;
    }
    if (!arr[index]) {
        return;
    }
    arr[index](function () {
        index++;
        if (index === arr.length) {
            callback();
        }
        else {
            seq(arr, callback, index);
        }
    });
}
